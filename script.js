        function showCategory(event, category) {
            event.preventDefault();
            const gallery = document.getElementById('gallery');
            gallery.innerHTML = '';
            images[category].forEach(src => {
            const imgContainer = document.createElement('div');
            imgContainer.style.position = 'relative';
            imgContainer.style.width = '200px';
            imgContainer.style.height = '150px';

            const loader = document.createElement('div');
            loader.style.position = 'absolute';
            loader.style.top = '50%';
            loader.style.left = '50%';
            loader.style.transform = 'translate(-50%, -50%)';
            loader.style.border = '4px solid #f3f3f3';
            loader.style.borderTop = '4px solid #3498db';
            loader.style.borderRadius = '50%';
            loader.style.width = '30px';
            loader.style.height = '30px';
            loader.style.animation = 'spin 1s linear infinite';

            const img = document.createElement('img');
            img.src = src;
            img.alt = category;
            img.style.width = '100%';
            img.style.height = '100%';
            img.style.objectFit = 'cover';
            img.style.borderRadius = '10px';
            img.style.opacity = '0';
            img.onload = () => {
                img.style.opacity = '1';
                loader.style.display = 'none';
            };
            img.onclick = () => showPreview(src);

            imgContainer.appendChild(loader);
            imgContainer.appendChild(img);
            gallery.appendChild(imgContainer);
            });
        }

        // Add CSS for loader animation
        const style = document.createElement('style');
        style.innerHTML = `
            @keyframes spin {
            0% { transform: rotate(0deg); }
            100% { transform: rotate(360deg); }
            }
        `;
        document.head.appendChild(style);

        function showPreview(src) {
            const preview = document.getElementById('preview');
            const previewImg = document.getElementById('preview-img');
            previewImg.src = src;
            $('#preview').modal('show'); // Use Bootstrap modal to display the preview
        }

        function closePreview() {
            const preview = document.getElementById('preview');
            preview.style.display = 'none';
        }

        function downloadImage() {
            const previewImg = document.getElementById('preview-img');
            const link = document.createElement('a');
            link.href = previewImg.src;
            link.download = '';
            link.target = '_blank';
            link.click();
        }
